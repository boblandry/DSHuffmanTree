package entity;

/**
 * @author LichKing
 * @description 存放 符号 和 其编码 还有其在原字符串中出现的次数的类
 */
public class HuffmanCode {

	private String name;
	
	private String code;
	
	private int num;

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
	
	
	
	
}
